class Stats {
  int id;
  int pokemonId;
  int hp;
  int attack;
  int defense;
  int spAttack;
  int spDefense;
  int speed;

  Stats({
    required this.id,
    required this.pokemonId,
    required this.hp,
    required this.attack,
    required this.defense,
    required this.spAttack,
    required this.spDefense,
    required this.speed,
  });
}
